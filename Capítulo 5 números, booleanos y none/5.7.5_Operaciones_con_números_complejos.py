"""También se pueden hacer operaciones como, suma, resta, multiplicación y división
haciendo uso de números complejos por ejemplo:"""

c1 = 1j
c2 = 2j
c3 = c1 * c2
print(c3)
print(type(c3))

"""También puede convertir otro número o una cadena en un número complejo usando
la función complex (). Por ejemplo"""

print('------'*20)
print(complex(5))

# Otra alternativa es:
x = complex(89)
print(x)

